package U1A7;

/**
 *
 * @author Xoel Vazquez Dios
 */
public abstract class Figura {
    private int x,y;

    public Figura(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
    
    public abstract double area();

}
